n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
soma = n1+n2
subtracao = n1-n2
multiplicacao = n1*n2
divisao = n1/n2
divint = n1//n2
modulo = n1%n2
expon = n1**n2

print('A soma é: {}'.format(soma), end=' -> ')
print('A subtração é: {}'.format(subtracao), end=' -> ')
print('A multiplicação é: {}'.format(multiplicacao))
print('A divisão é: {:.3f}'.format(divisao))
print('A divisão inteira é: {}'.format(divint))
print('O resto da divisão é: {}'.format(modulo))
print('A potên\ncia é: {}'.format(expon))



























